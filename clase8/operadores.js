//Operadores aritméticos

let a = 20;
let b = 2;
console.log(a + b); //Suma
console.log(a - b); //Resta
console.log(a * b); //Multiplicación
console.log(a / b); //División
console.log(a % b); //Módulo

//Operadores de comparación
console.log(a > b); //True a es mayor que b
console.log(a < b); //false a no es menor que b (es mayor)
console.log(a === "20"); //false a es number y "10" es string (cadena de texto)

//Operadores lógicos
console.log(true && false); //&& (AND): me devuelve sólo true si ambos valores son true.