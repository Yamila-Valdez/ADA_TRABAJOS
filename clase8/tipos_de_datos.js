//Definimos diferentes tipos de datos

let numero = 32; //Número
let texto = "Hola Mundo"; //Cadena de texto
let booleano = true; //Booleano (verdadero=true o falso=false)
let indefinido; //Variable no inicializada (indefinida)
let nulo = null; //Valor nulo (vacio)
let objeto = {nombre: "Yam", edad: 32}; //Objeto con propiedades

//Mostrar los valores y explicamos su tipo

console.log("Número: ", numero); //Esto es un número
console.log("Texto: ", texto); //Esto es un texto
console.log("Booleano: ", booleano); //Esto es un booleano (v o f)
console.log("Indefinido: ", indefinido); //Esto es indefinido (la variable no tiene valor)
console.log("Nulo: ", nulo); //Esto es nulo (representa un valor vacío)
console.log("Objeto: ", objeto); //Esto es un objeto (con nommbre y edad) 